#!/usr/bin/env python3
""" Module with a simple addition function. """


def add(a: float, b: float) -> float:
    """
    Add two floating-point numbers.

    Args:
        a (float): The first operand.
        b (float): The second operand.

    Returns:
        float: The sum of the two input numbers.
    """
    return (a + b)
